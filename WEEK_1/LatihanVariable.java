public class LatihanVariable{
    public static void main(String[] args){
        //deklarasi variable x dan y
        int x = 100;
        int y = 200;
        int total = x + y;
        System.out.println("Nilai x adalah = " + x + " dan Nilai y adalah = " + y);
        System.out.println("Nilai x + y adalah = " + (x + y));
        System.out.println("Nilai x + y adalah = " + total);
        System.out.println("Total = " + x + " + " + y + " = " + total);
    }
}
