import java.util.Scanner;
public class LatihanKonversi{
    public static void main (String[] args){
        int jam;
        int menit;
        int detik;
        int h1,h2,h3;
        
        Scanner inputan = new Scanner(System.in);
        System.out.print ("Masukan Jam Yang Ingin Dikonversi = ");
        jam = inputan.nextInt();
        System.out.print ("Masukan Menit Yang Ingin Dikonversi = ");
        menit = inputan.nextInt();
        System.out.print ("Masukan Detik Yang Ingin Dikonversi = ");
        detik = inputan.nextInt();
        
        h1 = (jam * 3600);
        h2 =  (menit * 60);
        h3 = (h1 + h2 + detik);
        System.out.print("\n");
        System.out.println ("Total Waktu " + jam + ":" + menit + ":"+ detik + " Dalam Detik = " + h3 );
        
    }
}
