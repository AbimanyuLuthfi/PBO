public class OperatorAssignments {

    public static void main(String[] args){

        // Operator assignment

        // Assignment operator jumlah
        int var1 = 1;
        var1 += 10; // var1 = var1 + 10; "menambahkan nilai var1 dengan nilai 10"
        System.out.println("nilai a = " + var1);

        // Assignment operator pengurangan
        int var2 = 100;
        var2 -= 25; // var2 = var2 - 25; "mengurangi nilai var2 dengan nilai 25"
        System.out.println("nilai b = " + var2);

        // Assignment operator perkalian
        int var3 = 100;
        var3 *= 20; // var3 = var3 x 20; "mengalikan nilai var3 dengan nilai 20"
        System.out.println("nilai c = " + var3);

        // Assignment operator pembagian
        int var4 = 100;
        var4 /= 20; // var4 = var4 / 20; "membagi nilai var4 dengan nilai 20"
        System.out.println("nilai d = " + var4);

        // Assignment operator modulus
        int var5 = 10;
        var5 %= 7; // var5 = var5 % 7; "modulo dari nilai var5 dengan 7"
        System.out.println("nilai e = " + var5);


    }
}
